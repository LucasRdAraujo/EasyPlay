package br.edu.infnet.springintrojsp.service;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import br.edu.infnet.springintrojsp.model.TextChannel;
import br.edu.infnet.springintrojsp.repository.TextChannelRepository;

@Service
public class TextChannelServiceImpl implements TextChannelService {

    @Autowired
    private TextChannelRepository textChannelRepository;

    @Override
    public TextChannel store(TextChannel textChannel) {
        return textChannelRepository.save(textChannel);
    }

    @Override
    public TextChannel getTextChannelById(String id) {
        return textChannelRepository.getById(id);
    }
    
}
